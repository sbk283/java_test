package org.example;

public class Main {
    public static void main(String[] args) {
        int a;
        a = 5;
        System.out.println(a);
        System.out.println(a + 10);

        int b = 10;

        System.out.println("a : " + a);
        System.out.println("b : " + b);

        a = 10;
        b = 5;

        System.out.println("a : " + a);
        System.out.println("b : " + b);
    }
}